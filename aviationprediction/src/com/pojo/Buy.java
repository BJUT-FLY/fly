package com.pojo;

public class Buy {
	private String buy_nbr;
	private int day_id;
	private int cnt;
	private float round;
	public String getBuy_nbr() {
		return buy_nbr;
	}
	public void setBuy_nbr(String buy_nbr) {
		this.buy_nbr = buy_nbr;
	}
	public int getDay_id() {
		return day_id;
	}
	public void setDay_id(int day_id) {
		this.day_id = day_id;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public float getRound() {
		return round;
	}
	public void setRound(float round) {
		this.round = round;
	}
	
}
